package hr.java.vjezbe.iznimke;

public class VisokaTemperaturaException extends Exception {

	private static final long serialVersionUID = -4709057145062952244L;

	public VisokaTemperaturaException() {
		super();
	}

	public VisokaTemperaturaException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
	
	public VisokaTemperaturaException(String arg0) {
		super(arg0);
	}
	
	public VisokaTemperaturaException(Throwable arg0) {
		super(arg0);
	}
	
}
